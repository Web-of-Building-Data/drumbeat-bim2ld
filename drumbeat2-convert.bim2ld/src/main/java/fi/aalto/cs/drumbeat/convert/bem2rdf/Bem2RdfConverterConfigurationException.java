package fi.aalto.cs.drumbeat.convert.bem2rdf;

public class Bem2RdfConverterConfigurationException extends Bem2RdfConverterException {

	private static final long serialVersionUID = 1L;

	public Bem2RdfConverterConfigurationException() {
	}

	public Bem2RdfConverterConfigurationException(String message) {
		super(message);
	}

	public Bem2RdfConverterConfigurationException(Throwable cause) {
		super(cause);
	}

	public Bem2RdfConverterConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public Bem2RdfConverterConfigurationException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
